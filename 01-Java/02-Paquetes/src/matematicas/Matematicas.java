public class Matematicas {
    
    public static float suma(float a, float b) {
        return a+b;
    }
    
    public float resta(float a, float b) {
        return a-b;
    }
    
    public float multiplicacion (float a, float b){
        return a*b;
    }
    
    public float division(float a, float b){
        return a/b;
    }
}
